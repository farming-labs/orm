for (const [target, factory] of [
  ["sqlite", createLocalSqliteOrm],
  ["postgresql", createLocalPostgresOrm],
  ["mysql", createLocalMysqlOrm],
] as const satisfies ReadonlyArray<readonly [PrismaTarget, RuntimeFactory]>) {
  describe.runIf(shouldRunTarget(target))(`${target} local Prisma integration`, () => {
    it(
      "exposes the live Prisma client on orm.$driver",
      async () => {
        const { orm, prisma, close } = await factory();
        try {
          expect(orm.$driver.kind).toBe("prisma");
          expect(orm.$driver.client).toBe(prisma);
          expect(orm.$driver.capabilities).toEqual({
            supportsNumericIds: false,
            supportsJSON: true,
            supportsDates: true,
            supportsBooleans: true,
            supportsTransactions: true,
            supportsJoin: false,
            nativeRelationLoading: "partial",
          });
          expect(Object.isFrozen(orm.$driver)).toBe(true);
          expect(Object.isFrozen(orm.$driver.capabilities)).toBe(true);
        } finally {
          await close();
        }
      },
      LOCAL_TIMEOUT_MS,
    );

    it(
      "creates and uses a real generated PrismaClient against a real local database",
      async () => {
        const PrismaClient = getGeneratedPrismaClient(target);
        const { prisma, close } = await factory();
        try {
          expect(typeof PrismaClient).toBe("function");
          expect(typeof prisma.$connect).toBe("function");
          expect(typeof prisma.$queryRawUnsafe).toBe("function");
          await expect(prisma.$queryRawUnsafe("SELECT 1")).resolves.toBeDefined();
        } finally {
          await close();
        }
      },
      LOCAL_TIMEOUT_MS,
    );

    it(
      "keeps read-only driver capabilities inside a real Prisma transaction",
      async () => {
        const { orm, close } = await factory();
        try {
          await orm.transaction(async (tx) => {
            expect(tx.$driver.kind).toBe("prisma");
            expect(tx.$driver.client).not.toBe(orm.$driver.client);
            expect(tx.$driver.capabilities.supportsTransactions).toBe(true);
            expect(tx.$driver.capabilities.nativeRelationLoading).toBe("partial");
            expect(tx.$driver.capabilities).toEqual(orm.$driver.capabilities);
            expect(Object.isFrozen(tx.$driver)).toBe(true);
            expect(Object.isFrozen(tx.$driver.capabilities)).toBe(true);
          });
        } finally {
          await close();
        }
      },
      LOCAL_TIMEOUT_MS,
    );

    it(
      "supports one-to-one and one-to-many reads against a real local database",
      async () => {
        const { orm, close } = await factory();
        try {
          await assertOneToOneAndHasManyQueries(orm, expect);
        } finally {
          await close();
        }
      },
      LOCAL_TIMEOUT_MS,
    );

    it(
      "supports belongsTo and many-to-many traversal against a real local database",
      async () => {
        const { orm, close } = await factory();
        try {
          await assertBelongsToAndManyToManyQueries(orm, expect);
        } finally {
          await close();
        }
      },
      LOCAL_TIMEOUT_MS,
    );

    it(
      "translates singular nested relation reads through the base Prisma delegate",
      async () => {
        const { prisma, close } = await factory();
        const instrumented = instrumentPrismaClient(prisma);
        const orm = createOrm({
          schema,
          driver: createPrismaDriver({
            client: instrumented.client as any,
          }),
        }) as RuntimeOrm;

        try {
          await seedAuthData(orm);
          instrumented.reset();

          const session = await orm.session.findUnique({
            where: {
              token: "session-2",
            },
            select: {
              token: true,
              user: {
                select: {
                  email: true,
                  profile: {
                    select: {
                      bio: true,
                    },
                  },
                },
              },
            },
          });

          const totalDelegateCalls = [...instrumented.counts.values()].reduce(
            (sum, value) => sum + value,
            0,
          );

          expect(session).toEqual({
            token: "session-2",
            user: {
              email: "ada@farminglabs.dev",
              profile: {
                bio: "Writes one storage layer for every stack.",
              },
            },
          });
          expect(totalDelegateCalls).toBe(1);
          expect(
            instrumented.counts.get("session.findFirst") ??
              instrumented.counts.get("session.findMany"),
          ).toBe(1);
          expect(instrumented.counts.get("user.findFirst") ?? 0).toBe(0);
          expect(instrumented.counts.get("profile.findFirst") ?? 0).toBe(0);
        } finally {
          await close();
        }
      },
      LOCAL_TIMEOUT_MS,
    );

    it(
      "translates simple many-to-many relation reads through the base Prisma delegate",
      async () => {
        const { prisma, close } = await factory();
        const instrumented = instrumentPrismaClient(prisma);
        const orm = createOrm({
          schema,
          driver: createPrismaDriver({
            client: instrumented.client as any,
          }),
        }) as RuntimeOrm;

        try {
          await seedAuthData(orm);
          instrumented.reset();

          const user = await orm.user.findUnique({
            where: {
              email: "ada@farminglabs.dev",
            },
            select: {
              email: true,
              organizations: {
                select: {
                  name: true,
                },
              },
            },
          });

          const totalDelegateCalls = [...instrumented.counts.values()].reduce(
            (sum, value) => sum + value,
            0,
          );
          const organizations = [...((user?.organizations ?? []) as Array<{ name: string }>)].sort(
            (left, right) => left.name.localeCompare(right.name),
          );

          expect({
            ...user,
            organizations,
          }).toEqual({
            email: "ada@farminglabs.dev",
            organizations: [{ name: "Acme" }, { name: "Farming Labs" }],
          });
          expect(totalDelegateCalls).toBe(1);
          expect(
            instrumented.counts.get("user.findFirst") ?? instrumented.counts.get("user.findMany"),
          ).toBe(1);
          expect(instrumented.counts.get("member.findMany") ?? 0).toBe(0);
          expect(instrumented.counts.get("organization.findMany") ?? 0).toBe(0);
        } finally {
          await close();
        }
      },
      LOCAL_TIMEOUT_MS,
    );

    it(
      "supports updates, upserts, deletes, and transaction rollback against a real local database",
      async () => {
        const { orm, close } = await factory();
        try {
          await assertMutationQueries(orm, expect, { expectTransactionRollback: true });
        } finally {
          await close();
        }
      },
      LOCAL_TIMEOUT_MS,
    );

    it(
      "supports compound-unique lookups and upserts against a real local database",
      async () => {
        const { orm, close } = await factory();
        try {
          await assertCompoundUniqueQueries(orm, expect);
        } finally {
          await close();
        }
      },
      LOCAL_TIMEOUT_MS,
    );

    it(
      "supports integer and json fields against a real local database",
      async () => {
        const { orm, close } = await factory();
        try {
          await assertIntegerAndJsonQueries(orm, expect);
        } finally {
          await close();
        }
      },
      LOCAL_TIMEOUT_MS,
    );

    it(
      "enforces model-level constraints against a real local database",
      async () => {
        const { orm, close } = await factory();
        try {
          await assertModelLevelConstraints(orm, expect);
        } finally {
          await close();
        }
      },
      LOCAL_TIMEOUT_MS,
    );
  });
}
