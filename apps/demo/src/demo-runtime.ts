function createIsolatedName(prefix: string) {
  return `${prefix}_${randomUUID().replace(/-/g, "")}`;
}

function assignDatabase(connectionString: string, databaseName: string) {
  const url = new URL(connectionString);
  url.pathname = `/${databaseName}`;
  return url.toString();
}

function assignMongoDatabase(connectionString: string, databaseName: string) {
  const url = new URL(connectionString);
  url.pathname = `/${databaseName}`;
  return url.toString();
}

function formatLocalRuntimeError(label: string, error: unknown, hint: string) {
  const message = error instanceof Error ? error.message : String(error);
  return new Error(`${label} demo runtime could not connect. ${hint}\nOriginal error: ${message}`);
}

function toDirectCheck(
  row?: { id?: string | null; email?: string | null; email_address?: string | null } | null,
) {
  if (!row?.id) return null;
  return {
    id: row.id,
    email: row.email ?? row.email_address ?? "",
  };
}

async function applyStatements(
  execute: (statement: string) => Promise<unknown> | unknown,
  sql: string,
) {
  const statements = sql
    .split(";")
    .map((statement) => statement.trim())
    .filter(Boolean);

  for (const statement of statements) {
    await execute(`${statement};`);
  }
}

function asMysqlConnectionLike(connection: mysql.PoolConnection): MysqlConnectionLike {
  return {
    execute(sql, params) {
      return connection.execute(sql as string, params as any) as Promise<[any, unknown]>;
    },
    beginTransaction() {
      return connection.beginTransaction();
    },
    commit() {
      return connection.commit();
    },
    rollback() {
      return connection.rollback();
    },
    release() {
      connection.release();
    },
  };
}

function asMysqlPoolLike(pool: mysql.Pool): MysqlPoolLike {
  return {
    execute(sql, params) {
      return pool.execute(sql as string, params as any) as Promise<[any, unknown]>;
    },
    async getConnection() {
      return asMysqlConnectionLike(await pool.getConnection());
    },
  };
}

function asMongooseModelLike(model: mongoose.Model<any>) {
  return model as unknown as MongooseModelLike;
}

