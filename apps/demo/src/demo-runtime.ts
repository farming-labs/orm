async function resetPrismaDatabase(prisma: InstanceType<typeof PrismaClient>) {
  await prisma.account.deleteMany();
  await prisma.session.deleteMany();
  await prisma.profile.deleteMany();
  await prisma.user.deleteMany();
}

async function alwaysAvailable() {
  return {
    available: true,
  } satisfies AvailabilityResult;
}

async function probePostgres(): Promise<AvailabilityResult> {
  const pool = new Pool({ connectionString: pgAdminUrl });

  try {
    await pool.query("select 1");
    return {
      available: true,
    };
  } catch (error) {
    return {
      available: false,
      reason: formatLocalRuntimeError(
        "PostgreSQL",
        error,
        `Set FARM_ORM_DEMO_PG_ADMIN_URL or FARM_ORM_LOCAL_PG_ADMIN_URL if your local admin URL is not ${pgAdminUrl}.`,
      ).message,
    };
  } finally {
    await pool.end().catch(() => undefined);
  }
}

